#A version manager basically helps us when we have different files that
#run in different versions of Ruby. For ex, one runs in 2.4.5 and another in
#2.6

=begin
RVM is the simplest way to change between the different rubies
in RVM, you can install different Rubies,
switch between them, and install gems for a specific Ruby.
If you're ever confused about which Ruby you're using, just type which ruby
from the command line. If the returned path contains a .rvm directory, then
you're already using RVM. If it doesn't, then you haven't activated RVM yet.
=end
